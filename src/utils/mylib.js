export const REGEX_EMAIL_VALIDATION =
  /^[a-zA-Z0-9.!#$%&'*+/=?^_`{|}~-]+@[a-zA-Z0-9-]+(?:\.[a-zA-Z0-9-]+)*$/;

//password recognition for atleast 8 characters, atleast 1 number, atleast 1 special character, atleast 1 letter
export const REGEX_PASSWORD_VALIDATION =
  /^(?=.*[A-Za-z])(?=.*\d)(?=.*[@$!%*#?&])[A-Za-z\d@$!%*#?&]{8,}$/;
